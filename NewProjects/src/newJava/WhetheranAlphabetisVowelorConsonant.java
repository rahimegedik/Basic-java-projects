package newJava;

public class WhetheranAlphabetisVowelorConsonant {

	public static void main(String[] args) {
		char x = 'i';
		if (x == 'a' || x == 'e' || x == 'ý' || x == 'i' || x == 'o' || x == 'ö' || x == 'u' || x == 'ü') {
			System.out.println("vowel");
		} else {
			System.out.println("consonant");
		}

	}

}
